"""
    Questao 02 - Faça um Programa que peça um valor e mostre na 
    tela se o valor é positivo ou negativo.
"""

num = float(input("\nDigite um numero: "))

if num > 0: 
    print("\nNúmero positivo")
else:
    print("\nNúmero negativo")

